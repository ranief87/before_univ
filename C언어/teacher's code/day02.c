	printf("%s: %d\n", "국어", 81);
	printf("%s%d\n", "수학: ",   64);
	printf("영어: %d\n", 61);
	printf("%s: %d\n", "합계", 81 + 64 + 61);
	printf("%s: %.2lf\n", "평균", 68.67);
	printf("학점: %c\n", 'D');
	printf("학점: %s\n", "D");
	printf("학점: %c\n", 68);

