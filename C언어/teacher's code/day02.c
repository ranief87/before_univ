//5.서식 지정자("%문자", 서식지정자에 알맞는 값)
//printf() 함수 안에 "~"에서 어떠한 데이터를 가져와서 출력 할 때,
//해당 위치에 데이터 타입에 맞는 서식지정자를 사용

/*
자주 쓸 서식지정자
%d		: 10진정수(Decimal) (...,-1,0,1,...) ('A','B','a',...) -> ASCII코드(65,66,97,...)
%lf		: 긴실수(Long Float) (..,-2.4,3.1415,2.05, 3.0,...) (소주점이 있는 숫자)
%f		: 실수(Float)  (위와 동일)
%c		: 문자(Charactor, 영문 1자, 영문 1Byte) 표기: '?'
		: 한글은 영문 2자, 2Byte이기 때문에 "%d", "%c"는 사용 불가능
%s		: 문자열(String, 영문 2Byte이상)
*/

//진수표현
/*
%o		: 8진수(Octal)  (표현식: 0(숫자))  (0100,0101) -> 10진수: 64, 65
%x		: 16진수(Hexa_Decimal)  (표현식: 0x(숫자))  (0x40,0x41) -> 10진수: 64, 65
*/

//기타
/*
%p		: 포인터 정수(16진수)  -> 포인터에서 이용(주소값)
%e		: 지수형 부동소수(로그, e^x)
%G,%g   : 숫자를 있는 그대로 표현
*/


